package CycleTask;

public class ShowArrayElementsMirror {

    // вывести в консоль все элементы массива в обратном порядке

    public void metprintAllElementsMirrorOder() {
        int[] showArrayElementsMirror = {1, 2, 4, 5, 3, 7};
        for (int i = showArrayElementsMirror.length - 1; i >= 0; i++) {
            System.out.print(showArrayElementsMirror[1]);
        }
    }
}
